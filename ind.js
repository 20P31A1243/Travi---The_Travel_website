function myFunction() {
  var x = document.getElementById("obob");
  var y=document.getElementById("b1");
  if (x.style.display === "none") {
    x.style.display = "block";
    y.style.display="none";
    setTimeout(myFunction,4000);
  } else {
    x.style.display = "none";
    y.style.display="block";
  }
}
